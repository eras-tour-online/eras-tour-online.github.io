function regsucc() {
    document.getElementById("registration-page").style.display="none";
              document.getElementById("registration-successful").style.display="block";
              document.getElementById("registration-unsuccessful").style.display="none";
              window.scrollTo(0,0);
}
function regunsucc() {
    document.getElementById("registration-page").style.display="none";
              document.getElementById("registration-successful").style.display="none";
              document.getElementById("registration-unsuccessful").style.display="block";
              window.scrollTo(0,0);
}
